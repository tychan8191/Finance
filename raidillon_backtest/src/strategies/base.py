class SingleOptionStrategy(BaseStrategy):
    """
    Strategy for single-leg option positions (protective puts, etc.).
    """
    
    def check_entry(self, snapshot: MarketSnapshot) -> Optional[Signal]:
        """Evaluate entry for single option position."""
        
        if self._entry_signal_generated:
            return None
        
        if not self.config.enabled:
            return None
        
        current_date = snapshot.timestamp.date() if isinstance(snapshot.timestamp, datetime) else snapshot.timestamp
        if not self.is_in_entry_window(current_date):
            return None
        
        # For hedges like SPY_PROTECTIVE_PUT, entry is usually immediate
        for condition in self.config.entry_conditions:
            if condition.get('condition') == 'always':
                self._entry_signal_generated = True
                
                structure = self.get_target_structure(snapshot)
                
                return Signal(
                    signal_type=SignalType.ENTRY,
                    strategy_name=self.name,
                    underlying=self.config.underlying,
                    timestamp=snapshot.timestamp,
                    target_structure=structure,
                    urgency=3,
                    notes=f"Hedge entry: {self.name}",
                )
        
        return None
    
    def check_exit(
        self, 
        snapshot: MarketSnapshot, 
        position: Any,
        position_pnl_pct: float
    ) -> Optional[Signal]:
        """Check exit conditions for single option."""
        
        exit_rules = self.config.exit_rules
        
        # Check monetize condition (for protective puts)
        monetize = exit_rules.get('monetize', {})
        if monetize:
            condition = monetize.get('condition', '')
            threshold = monetize.get('threshold')
            
            if condition == 'spy_down_pct':
                # Check if SPY has dropped significantly
                spy_price = snapshot.get_price('SPY')
                # Would need entry price tracking - use P&L as proxy
                if position_pnl_pct >= 50:  # Put is profitable, SPY must be down
                    return Signal(
                        signal_type=SignalType.EXIT_PARTIAL,
                        strategy_name=self.name,
                        underlying=self.config.underlying,
                        timestamp=snapshot.timestamp,
                        exit_reason=ExitReason.TAKE_PROFIT,
                        exit_pct=0.5,
                        urgency=3,
                        notes="Monetizing protective put gain",
                    )
        
        return None
    
    def get_target_structure(self, snapshot: MarketSnapshot) -> Dict[str, Any]:
        """Build target structure for single option."""
        
        current_date = snapshot.timestamp.date() if isinstance(snapshot.timestamp, datetime) else snapshot.timestamp
        target_expiration = current_date + timedelta(days=self.config.expiration_target_dte)
        
        # Adjust to Friday
        days_until_friday = (4 - target_expiration.weekday()) % 7
        if days_until_friday == 0 and target_expiration.weekday() != 4:
            days_until_friday = 7
        target_expiration = target_expiration + timedelta(days=days_until_friday)
        
        # Calculate strike based on OTM percentage
        underlying_price = snapshot.get_price(self.config.underlying)
        strike = self.config.long_strike
        
        if strike is None and underlying_price:
            # Calculate OTM strike
            legs = self.config.raw_config.get('structure', {}).get('legs', {})
            long_leg = legs.get('long', {})
            otm_pct = long_leg.get('strike_otm_pct', 5)
            
            # For puts, OTM means below current price
            if self.config.right == 'P':
                strike = underlying_price * (1 - otm_pct / 100)
            else:
                strike = underlying_price * (1 + otm_pct / 100)
            
            # Round to standard strike
            strike = round(strike / 5) * 5
        
        return {
            'structure_type': self.config.structure_type,
            'underlying': self.config.underlying,
            'expiration': target_expiration,
            'strike': strike,
            'right': self.config.right,
            'quantity': 1,
        }


def create_strategy(config: StrategyConfig) -> BaseStrategy:
    """
    Factory function to create the appropriate strategy type.
    
    Args:
        config: StrategyConfig for the strategy
        
    Returns:
        Appropriate BaseStrategy subclass instance
    """
    structure_type = config.structure_type.lower()
    
    if 'spread' in structure_type:
        return VerticalSpreadStrategy(config)
    elif structure_type in ('long_put', 'long_call'):
        return SingleOptionStrategy(config)
    else:
        # Default to vertical spread
        return VerticalSpreadStrategy(config)


